package com.java.day4;

public class DemoMethods {
	public static void test1() {
		//this is non -return type method
		System.out.println("this is a static void method");
	}

	public void test2() {//this is non-return type method
		System.out.println("this is static void method2");
	}
//this is integer type return method
	public int test3() {
		return 25;
	}
	//this is string type return method
	public String test4() {
		String name="srinath";
		return name;
	}
	//this is char type return method
	public char test5() {
		char n='a';
		return n;
	}
	//this is boolean return type method
	public boolean test6() {
		boolean venu=true;
		return venu;
	}
	//this is decimal type return method
	public float test7() {
		return 34.5f;
	}
}
